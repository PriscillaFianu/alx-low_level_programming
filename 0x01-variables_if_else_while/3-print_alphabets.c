#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: Prints all alphabets in lowercase
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	int n = 97;
	int r = 65;

	while (n <= 122)
	{
	putchar (n);
	}
	putchar ('\n');
	while (r = 90)
	{
	putchar (r);
	}
	putchar ('\r');
	return (0);
}
